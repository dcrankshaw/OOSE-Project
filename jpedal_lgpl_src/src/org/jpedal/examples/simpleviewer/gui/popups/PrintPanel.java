    private int getDefaultResolutionIndex() {
        //get print service
        PrintService[] ps = PrintServiceLookup.lookupPrintServices(null,null);
        PrintService p=null;
        for (int i=0; i<ps.length; i++) {
            if (ps[i].getName().equals(printerName.getSelectedItem()))
                p = ps[i];
        }

        //get available resolutions
        PrinterResolution[] resolutions = (PrinterResolution[])p.getSupportedAttributeValues(PrinterResolution.class, DocFlavor.SERVICE_FORMATTED.PAGEABLE, null);

        if (resolutions == null)
            return 0;

        if (defaultResolution == -1)
                defaultResolution = 600;

        //find nearest resolution
        int minDiff = Integer.MAX_VALUE;
        int indexToUse = 0;
        for (int i=0; i<resolutions.length; i++) {
            PrinterResolution res = resolutions[i];
            int cfDiff = res.getCrossFeedResolution(PrinterResolution.DPI)-defaultResolution;
            if (cfDiff < 0)
                cfDiff = -cfDiff;
            int fDiff = res.getFeedResolution(PrinterResolution.DPI)-defaultResolution;
            if (fDiff < 0)
                fDiff = -fDiff;

            if (cfDiff+fDiff < minDiff) {
                minDiff = cfDiff+fDiff;
                indexToUse = i;
            }
        }

        return indexToUse;
    }

